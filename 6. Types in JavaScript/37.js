// Array lastindexof()
// The lastIndexOf() method returns the index of the last
//  occurrence of a specified element in the array.

// Example;
const arr = ["s", "u", "h", "a", "i", "l", "r", "o", "n", "s", "h", "a", "n"];

//  Couting Index starts from 1 to 12 from left to right
// But Finding element starts from right to left
console.log(arr.lastIndexOf("n")); //12
console.log(arr.lastIndexOf("a")); // 11

// It accepts 2 parameters

// arr.lastindexof(ele,startfromindex)
