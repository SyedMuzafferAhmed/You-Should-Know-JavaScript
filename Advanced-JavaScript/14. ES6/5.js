// Template Literals

const name = "Suhail";
console.log(`Hello ${name}`);

// Using Template Literals You can Allow Variables and Embedded Expressions to use in Strings

let cost = 100;
console.log(`The Price of Product is ${cost * 10}`);

// Multi Line Using + Operator

const msg =
  "This is a Long Message\n" +
  "Its In Next Line As You Can See It\n" +
  "Hello\n" +
  "No.4\n" +
  "No.5";
console.log(msg);

